    private void listar() {
    // Obtener los datos del negocio
    Map<String, Object[]> datos = (Map<String, Object[]>) negocio.listar();

    // Obtener el modelo de la tabla
    DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
    
    // Limpiar las filas existentes
    modelo.setRowCount(0);

    // Recorrer el Map y agregar cada fila al modelo
    for (Map.Entry<String, Object[]> entry : datos.entrySet()) {
        Object[] fila = entry.getValue(); // obtiene el arreglo de objetos para cada fila
        modelo.addRow(fila);
    }
 }

    
    
